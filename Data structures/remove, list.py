#remove from the list

r= [1, 2, 3, 3, 5, 66, 66, ['a', 700]]

r.remove(66)        #remove the first element which is 66
print(r)


r.remove (['a', 700])
print(r)


r.pop(0)    #it removes the value within index 0
print(r)


r.pop ()    #if u don't get a value to it, it removes the last itemS
print(r)

r.clear()   #it clears the whole list
print(r)




